package com.example.employeetripsandtramps;

public class FinancialReportSearch {
    String TRANSECTION_ID;
    String USERNAME;
    String INCOME;
    String EXPENSES;
    String PROFIT;

    public String getTRANSECTION_ID() {
        return TRANSECTION_ID;
    }

    public void setTRANSECTION_ID(String TRANSECTION_ID) {
        this.TRANSECTION_ID = TRANSECTION_ID;
    }

    public String getUSERNAME() {
        return USERNAME;
    }

    public void setUSERNAME(String USERNAME) {
        this.USERNAME = USERNAME;
    }

    public String getINCOME() {
        return INCOME;
    }

    public void setINCOME(String INCOME) {
        this.INCOME = INCOME;
    }

    public String getEXPENSES() {
        return EXPENSES;
    }

    public void setEXPENSES(String EXPENSES) {
        this.EXPENSES = EXPENSES;
    }

    public String getPROFIT() {
        return PROFIT;
    }

    public void setPROFIT(String PROFIT) {
        this.PROFIT = PROFIT;
    }
}
