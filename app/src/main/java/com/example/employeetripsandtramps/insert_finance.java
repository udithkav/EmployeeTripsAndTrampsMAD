package com.example.employeetripsandtramps;

public class insert_finance {
    private Integer TRANSECTION_ID;
    private String USERNAME;
    private Integer INCOME;
    private Integer EXPENSES;
    private Integer PROFIT;

    public insert_finance() {
    }

    public Integer getTRANSECTION_ID() {
        return TRANSECTION_ID;
    }

    public void setTRANSECTION_ID(Integer TRANSECTION_ID) {
        this.TRANSECTION_ID = TRANSECTION_ID;
    }

    public String getUSERNAME() {
        return USERNAME;
    }

    public void setUSERNAME(String USERNAME) {
        this.USERNAME = USERNAME;
    }

    public Integer getINCOME() {
        return INCOME;
    }

    public void setINCOME(Integer INCOME) {
        this.INCOME = INCOME;
    }

    public Integer getEXPENSES() {
        return EXPENSES;
    }

    public void setEXPENSES(Integer EXPENSES) {
        this.EXPENSES = EXPENSES;
    }

    public Integer getPROFIT() {
        return PROFIT;
    }

    public void setPROFIT(Integer PROFIT) {
        this.PROFIT = PROFIT;
    }
}
